def visa_price_reply(text: str) -> str | None:
    """Return a scoped visa price answer, or None when the message is not a price question."""
    lowered = (text or "").lower()
    if not PRICE_INTENT_RE.search(lowered):
        return None
    for _country, aliases, reply in _COUNTRY_PRICES:
        if any(alias in lowered for alias in aliases):
            return reply
    return "Подскажите, по какой стране нужна цена? Напишите страну, и я назову официальный прайс только по ней."


def self_visa_reply(text: str, *, already_sent: bool = False) -> str | None:
    """One soft retention message for clients who want to apply without support."""
    if not SELF_VISA_RE.search(text or ""):
        return None
    if already_sent:
        return "Понимаю. Передам менеджеру, он сможет коротко подсказать по рискам и дальше уже решите сами."
    return (
        "Понимаю, можно попробовать самостоятельно. Мы полезны тем, что проверяем анкету, "
        "снижаем риск ошибок и готовим к интервью. Хотите, я передам менеджеру на короткую консультацию?"
    )
